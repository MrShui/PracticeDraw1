public class Data {
    private String lable;
    private int number;

    public Data(String lable, int number) {
        this.lable = lable;
        this.number = number;
    }

    public String getLable() {
        return lable;
    }

    public void setLable(String lable) {
        this.lable = lable;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
